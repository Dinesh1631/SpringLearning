package Spring.spring.Services;

import Spring.spring.Shapes.Circel;
import Spring.spring.Shapes.Triangle;

public class shapeServices {
      private Circel circel;
      private Triangle triangle;
	public Circel getCircel() {
		return circel;
	}
	public void setCircel(Circel circel) {
		this.circel = circel;
	}
	public Triangle getTriangle() {
		return triangle;
	}
	public void setTriangle(Triangle triangle) {
		this.triangle = triangle;
	}
      
}
